/**Bài tập 4 : Tính diện tích, chu vi hình chữ nhật
 * input : chiều dài, chiều rộng
 * process : 
 * 1. lấy input 
 *  var length = 2
 *  var widths = 3
 * 2. 
 *  tạo biến acreage để lưu diện tích
 *  tạo biến perimeter để lưu chu vi
 * output : Tính diện tích, chu vi
 */
var length = 2
var widths = 3
var acreage = length * widths
var perimeter = (length + widths)*2
console.log('Diện tích hình chữ nhật:', acreage)
console.log('Chu vi hình chữ nhật:', perimeter)